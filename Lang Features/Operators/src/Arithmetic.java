public class Arithmetic {

    public static void main(String[] args) {
        int firstNumber = 100;
        int secondNumber = 10;

        // Arithmetic operators +, -, *, /, %
        System.out.println(firstNumber + secondNumber);
        System.out.println(firstNumber - secondNumber);
        System.out.println(firstNumber * secondNumber);
        System.out.println(firstNumber / secondNumber);
        System.out.println(firstNumber % secondNumber);
    }
}

public class Turnary {
    public static void main(String[] args) {

        int firstNumber = 100;
        int secondNumber = 10;

        // Turnary Operators ++, --, ?
        firstNumber++;
        secondNumber++;
        System.out.println(firstNumber);
        System.out.println(secondNumber);

        firstNumber = 100;
        var result = firstNumber <= 10 ? true:false;
        System.out.println(result);

    }
}
